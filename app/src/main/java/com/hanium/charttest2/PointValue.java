package com.hanium.charttest2;

public class PointValue {

    long xValue;
    int yValue;

    public PointValue() {

    }

    public PointValue(long xValue, int yValue) {
        this.xValue = xValue;
        this.yValue = yValue;
    }

    public long getxValue() {
        return xValue;
    }

    public int getyValue() {
        return yValue;
    }
}

